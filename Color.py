class Color:
    def grey(texto):
        return f"\033[90m{texto}\033[0m"

    def red(texto):
        return f"\033[91m{texto}\033[0m"
    
    def green(texto):
        return f"\033[92m{texto}\033[0m"

    def yellow(texto):
        return f"\033[93m{texto}\033[0m"

    def blue(texto):
        return f"\033[94m{texto}\033[0m"
    
    def purple(texto):
        return f"\033[95m{texto}\033[0m"